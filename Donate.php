    $product_name = $_POST['productname'];
    $name_id = $_POST['productstatus'];
    $description = $_POST['Note'];
    $price = $_POST['contactinfo'];
    
    // File upload handling
    $image_tmp_path = $_FILES['product_image']['tmp_name'];
    $base64_string = image_to_base64($image_tmp_path);

    // Prepare SQL query
    $query = "INSERT INTO donation ( productname,productstatus, note, contactinfo ,productimage) VALUES (  ?, ?, ?, ?, ?)";

    // Execute query
    $stmt = $conn->prepare($query);
    $stmt->execute([ $product_name, $name_id, $description, $price, $base64_string]);

    // Close connection
    $conn = null;

